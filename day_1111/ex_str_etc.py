# 이스케이스 문자
# - 문자열 안에서 특별한 기능을 하는 문자
# - 형식 : \알파벳 1개
# - 많이 사용되는 것
# - 중요 : \n 줄 바꿈 , \t 탭만큼 간격띄움 , \' 홑따옴표, \"" 쌍따옴표 , \\ \기호 삽입 \U - 유니코드 
