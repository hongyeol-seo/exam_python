# ---------------------------------------------------
# 문자열 str 타입 연산 수행
# ---------------------------------------------------

name = "Hong"
gender = "M"

# 산술연살 수행 -------------------------------------
# 덧셈 : str + str = 문자열 연결
print(f"{name} + {gender} = {name+gender}")
# print(f"{name} + {2022} = {name+2022}") #문자열끼리만 덧셈 가능
# 오직 str + int 안됨! 오직 str + str만 됨
print(f"{name} + {str(2022)} = {name+str(2022)}")


