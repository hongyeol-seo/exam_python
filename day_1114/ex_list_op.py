# ---------------------------------
# 리스트 자료형의 연산 => 덧셈, 곱셉
# -덧셈 : 리스트 + 리스트
# -곱셉 : 리스트 * 정수
# ---------------------------------

#str 타입 보기
msg1 = "Hello"
msg2 = "2022"

print('+' , msg1 + msg2)
print('*' , msg1 * 5)

# list 타입 보기 -----------------
nums1 = [1,3,5]
nums2 = ['a','b','c']

print('+',nums1+nums2)
print('*',nums2 * 3)
